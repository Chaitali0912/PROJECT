    sq = st.text_input("Ask a question or search for a concept within this document...", key=f"search_{title}")
    if sq:
        results = semantic_search(models['embedder'], text, [sq], top_k=2)
        if results and sq in results:
            for i, res in enumerate(results[sq]):
                st.info(f"**Match {i+1}** (Score: {res['score']:.2f}): {res['sentence']}")

