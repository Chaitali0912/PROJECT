@st.cache_resource
def init_models():
    return {
        "summarizer": load_summarizer(),
        "sentiment": load_sentiment_analyzer(),
        "emotion": load_emotion_analyzer(),
        "ner": load_ner_model(),
        "embedder": load_embedding_model()
    }

def build_markdown_report(title, summary, overall_sent, behavior, entities, emotion_dist, speakers):
    """Builds a markdown string for the downloadable report."""
    md = f"# NLP Analysis Report: {title}\n\n"
    md += f"## 📝 Abstractive Summary\n{summary}\n\n"
    md += f"## 📊 Core Metrics\n"
    md += f"- **Sentiment:** {overall_sent['label']} ({(overall_sent['score']*100):.1f}%)\n"
    md += f"- **Sentences:** {behavior['total_sentences']}\n"
    md += f"- **Avg Words/Sentence:** {behavior['avg_words_per_sentence']}\n\n"
    
    if emotion_dist:
        md += "## 🎭 Emotion Distribution\n"
        for em, score in emotion_dist.items():
            md += f"- **{em.capitalize()}:** {(score*100):.1f}%\n"
        md += "\n"

    if entities:
        md += "## 📇 Named Entities\n"
        for k, v in entities.items():
            if v:
                md += f"**{k}**: {', '.join(v)}\n\n"

    if speakers:
        md += "## 🗣️ Transcript Speakers\n"
        md += f"Detected {len(speakers)} speakers: {', '.join(speakers.keys())}\n\n"
    
    md += "---\n*Generated by Deep Text Analytics*"
    return md

@st.cache_data(show_spinner=False)
def compute_analytics(text):
    models = init_models()
    # 1. Preprocessing
    chunks = chunk_text(text, max_words=300)
    
    # 2. Analytics
    sum_text = []
    for ch in chunks: # Summarize all chunks
        sum_text.append(summarize_text(models['summarizer'], ch))
    final_summary = " ".join(sum_text)
    
    # If the combined summary is very long, summarize the summary to keep it clean
    if len(chunks) > 2 and len(final_summary.split()) > 250:
        final_summary = summarize_text(models['summarizer'], final_summary)
    
    keywords = extract_keywords(text, top_n=8)
    overall_sent = analyze_sentiment(models['sentiment'], text)
    time_series = track_sentiment_over_time(models['sentiment'], text)
    emotion_dist = analyze_emotion(models['emotion'], text)
    entities = extract_entities(models['ner'], text)
    behavior = analyze_behavior(text)
    traits = detect_personality(overall_sent, behavior, keywords)
    speakers = extract_speakers(text)
    
    return final_summary, keywords, overall_sent, time_series, emotion_dist, entities, behavior, traits, speakers

def run_analysis(title, text):
    with st.spinner(f"Crunching data for {title}..."):
        final_summary, keywords, overall_sent, time_series, emotion_dist, entities, behavior, traits, speakers = compute_analytics(text)
    
    models = init_models()
    # UI Rendering
    
    # Download Button
    report_md = build_markdown_report(title, final_summary, overall_sent, behavior, entities, emotion_dist, speakers)
    st.download_button(
        label=f"📥 Download Report ({title})",
        data=report_md,
        file_name=f"{title.replace(' ', '_')}_report.md",
        mime="text/markdown"
    )
    
    # Row 1: Summary & Personality
    r1c1, r1c2 = st.columns(2)
